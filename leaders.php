        The Superior Council of Defense adopted last June, the initial revised Plan of Reform few weeks ago. The new elements occurred in April 2012 in the territories of Rutshuru and Nyragongo therefore motivated the revision of the Plan. To comply with one hand, this revised plan and secondly, for the sake of gradually rejuvenate and increase the efficiency of the men who control the Military of the Democratic Republic of the Congo (FARDC), Joseph Kabila, in his dual capacity as Head of State and Supreme Commander of the FARDC, has taken a series of orders (twelve in total), made public for four days. Either last Sunday and Monday.</p>
      <p align="left">This is, inter alia, Ordinance No. 13/82 concerning forced retirement of General Officers of the FARDC, Ordinance No. 13/086 concerning forced retirement of senior officers of FARDC and Order 13/088 concerning forced retirement of Junior Officers of the FARDC. Not only. As some general officers of FARDC were promoted at the end of Ordinance No. 13/089 appointing in the category of General Officers of the FARDC. Add orders 13/091 appointing posthumously in the category of senior officers, Ordinance No. 13/090 appointing posthumously into the category of general officers. Finally, Ordinance No. 13/092 appointing posthumously in the category of junior officers of the FARDC.</p>
      <p align="left">Also, Joseph Kabila welcomed t he general, senior and junior officers who loyally and faithfully served the country but, to date, have been honorably retired. To these generals, senior and junior officers, Congolese President promises a retirement allowance and reintegration honorable in proportion to the rank of each. An allowance of end of honorable career for General Officers and fair for senior and junior officers. That, in accordance with Articles 195 and 197 of the Law on Military Statute.</p>
      <p align="left">After the series of public orders made on Sunday, two more were enacted late in the evening of Monday, July 8 on the airwaves of radio and public television, the Congolese National Radio Television (RTNC). This is the order of 7 July 2013 appointing the category of General Officers of the FARDC. In its edition of yesterday Tuesday, Forum des As was echoed in different nominations. Also, it presents newspaper writing all his apologies to all their readers consecutive some misspelled words because of decryption hazards.</p>
